//create class KilometersToMiles to convert distance in kilometers to miles
public class KilometersToMiles {
    public static void main (String args[]) {
	   
	   //creating variables to store distance in km and distance in miles 
       double distance = 10.8 ;
	   double distanceInMiles = distance * 0.621371 ;
	   
	   //Displaying distance in miles
	   System.out.println( "The distance 10.8 km in miles is " + distanceInMiles) ;
	}
}